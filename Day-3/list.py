# lists in python

# list is used to store multiple items in single variable

list = ['car','bike',1,True,3.4] # values can be boolean, number, float

# lists in python are ordered , changeable and allow duplicates

print(list[2]) # to acces item in list

print(list[2:5]) # we can take range as well

list[2] = 'truck' # we can change list

list[3:5] = ['apple','new bike']

print(list)

list.insert(3,'jeep') # to insert in a position

print(list)

list.append('oranges')

print(list)